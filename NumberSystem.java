public class NumberSystem {

    public static void main(String[] args) {
        // Binary to decimal number
        System.out.println(0b101); // output is 5
        System.out.println(0b1111); // output is 15

        // Octal to decimal number
        System.out.println(012); // output is 10
        System.out.println(0123); // output is 83

        // Hexadecimal to decimal number
        System.out.println(0xe); // output is 14
        System.out.println(0x10); // output is 16
    }
}